# Style

# PEP 8 è una guida di stile per il codice Python che fornisce linee guida su come scrivere codice leggibile e mantenibile. Alcuni dei punti principali della PEP 8 includono:
# - Usare spazi invece di tabulazioni per l'indentazione.
# - Usare 4 spazi per l'indentazione.
# - Limitare le righe a 79 caratteri.
# - Usare nomi di variabili e funzioni in minuscolo con parole separate da underscore (es. my_variable).
# - Usare nomi di classi in CamelCase (es. MyClass).
# - Usare spazi intorno agli operatori e dopo le virgole.
# - Usare spazi dopo i due punti in una dichiarazione di funzione o di classe.
# - Usare spazi dopo le parole chiave come if, for, while, ecc.
# - Usare spazi dopo le parole chiave come def, class, ecc.
# - Usare spazi dopo le parole chiave come return, yield, ecc.
# - Usare spazi dopo le parole chiave come import, from, ecc.
# - Usare spazi dopo le parole chiave come with, as, ecc.

